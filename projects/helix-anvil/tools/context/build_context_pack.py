#!/usr/bin/env python3
"""Canonical global substrate tool — see ~/shared/substrate/SUBSTRATE_PORTING_GUIDE.md.
Project-specific config in the CONFIG block below.

Generate the agent context pack for this project.

Outputs (all derived from PROJECT_STATE.json and the canonical docs):
  docs/context/AGENT_CONTEXT.md  - short "read me first" orientation. An
                                   AUTO region (regenerated from PROJECT_STATE)
                                   plus a preserved HUMAN region for prose.
  docs/context/SOURCE_MAP.md     - classification of the indexed corpus
                                   against PROJECT_STATE's canonical/historical
                                   doc lists.
  llms.txt                       - llms.txt-standard curated link index.
  llms-full.txt                  - full concatenation of canonical docs PLUS
                                   the active feature packet(s) for complete
                                   single-shot LLM ingestion.
  docs/context/.contextpack.hash - freshness marker over the pack inputs.

Improvement over prior versions:
  llms-full.txt now ALSO includes each active feature packet's files
  (requirements.md, design.md, tasks.md, acceptance.md, status.json), so a
  single-shot ingest gives an agent everything needed to continue work without
  a separate read pass over the feature directory.

Usage:
  python tools/context/build_context_pack.py
  python tools/context/build_context_pack.py --check   # exit 1 if stale

Stdlib only.
"""
from __future__ import annotations

import argparse
import hashlib
import json
import sys
from collections import Counter, defaultdict
from datetime import datetime, timezone
from pathlib import Path

# ---------------------------------------------------------------------------
# CONFIG — edit this block when porting to a new project.
# ---------------------------------------------------------------------------

REPO_ROOT = Path(__file__).resolve().parents[2]

# Output paths (relative to REPO_ROOT)
CTX        = REPO_ROOT / "docs" / "context" / "AGENT_CONTEXT.md"
SOURCE_MAP = REPO_ROOT / "docs" / "context" / "SOURCE_MAP.md"
LLMS       = REPO_ROOT / "llms.txt"
LLMS_FULL  = REPO_ROOT / "llms-full.txt"
HASH_FILE  = REPO_ROOT / "docs" / "context" / ".contextpack.hash"

# Input paths
STATE     = REPO_ROOT / "PROJECT_STATE.json"
INDEX_JSON = REPO_ROOT / "docs" / "DOCUMENT_INDEX.json"

# The 5 canonical files expected inside every feature packet directory.
# PORTING: if your packet convention differs, adjust this tuple.
PACKET_FILES = (
    "requirements.md",
    "design.md",
    "tasks.md",
    "acceptance.md",
    "status.json",
)

# Maximum characters to include per canonical doc in llms-full.txt.
# None = no limit. Reduce if you hit context-window limits.
LLMS_FULL_DOC_MAX_CHARS: int | None = None

# Maximum characters per feature packet file in llms-full.txt.
LLMS_FULL_PACKET_MAX_CHARS: int | None = 8000

# ---------------------------------------------------------------------------
# END CONFIG
# ---------------------------------------------------------------------------

AUTO_START  = ("<!-- AUTO:START - generated by build_context_pack.py; "
               "edit PROJECT_STATE.json, not here -->")
AUTO_END    = "<!-- AUTO:END -->"
HUMAN_START = "<!-- HUMAN:START -->"
HUMAN_END   = "<!-- HUMAN:END -->"


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def load_state() -> dict:
    if not STATE.exists():
        raise SystemExit(
            "PROJECT_STATE.json not found — create it before building the "
            "context pack.")
    return json.loads(STATE.read_text(encoding="utf-8"))


def read_text(rel_or_abs: str | Path,
              max_chars: int | None = None) -> str:
    """Read a file relative to REPO_ROOT (or as absolute path)."""
    p = Path(rel_or_abs)
    if not p.is_absolute():
        p = REPO_ROOT / p
    if not p.exists():
        return f"[MISSING: {rel_or_abs}]"
    data = p.read_text(encoding="utf-8", errors="replace")
    if max_chars is not None and len(data) > max_chars:
        return data[:max_chars].rstrip() + "\n\n[truncated by context pack]\n"
    return data


def canonical_doc_paths(state: dict) -> list[Path]:
    return [REPO_ROOT / d["path"] for d in state.get("canonical_docs", [])]


def active_packet_dirs(state: dict) -> list[Path]:
    return [REPO_ROOT / p["path"]
            for p in state.get("active_feature_packets", [])
            if p.get("path")]


def ratified_spec_paths(state: dict) -> list[Path]:
    """Ratified capability specs (substrate v2.2). Empty pre-v2.2."""
    return [REPO_ROOT / s["path"]
            for s in state.get("ratified_specs", [])
            if s.get("path")]


def pack_inputs_hash(state: dict) -> str:
    """Hash the SOURCE inputs that drive generation.

    AGENT_CONTEXT.md and SOURCE_MAP.md are excluded: they are generated
    OUTPUTs, so including them would cause the freshness hash to chase a
    moving target and flag harmless human-region edits as drift.
    """
    generated_outputs = {CTX.resolve(), SOURCE_MAP.resolve()}
    h = hashlib.sha256()
    h.update(STATE.read_bytes())
    if INDEX_JSON.exists():
        h.update(INDEX_JSON.read_bytes())
    for p in canonical_doc_paths(state):
        if p.resolve() in generated_outputs:
            continue
        if p.exists():
            h.update(p.name.encode())
            h.update(p.read_bytes())
    for p in ratified_spec_paths(state):
        if p.exists():
            h.update(p.name.encode())
            h.update(p.read_bytes())
    for d in active_packet_dirs(state):
        for fname in PACKET_FILES:
            fp = d / fname
            if fp.exists():
                h.update(fp.name.encode())
                h.update(fp.read_bytes())
    return h.hexdigest()


