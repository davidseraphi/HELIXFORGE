def write_llms(state: dict) -> None:
    proj    = state.get("project", {})
    posture = state.get("strategic_posture", {})
    name    = proj.get("name", "PROJECT")
    lines = [
        f"# {name}",
        "",
        f"> {proj.get('one_liner', '')}",
        "",
    ]
    if posture.get("north_star") or posture.get("hard_promise"):
        lines.append(
            f"North star: {posture.get('north_star', '')}. "
            f"Hard promise: {posture.get('hard_promise', '')}."
        )
        lines.append("")

    lines += ["## Canonical docs", ""]
    for d in state.get("canonical_docs", []):
        lines.append(f"- [{Path(d['path']).stem}]({d['path']}): "
                     f"{d.get('role', '')}")

    if state.get("ratified_specs"):
        lines += ["", "## Capability specs (ratified = current behavior)", ""]
        for s in state["ratified_specs"]:
            label = s.get("capability") or Path(s["path"]).parent.name
            st = s.get("status", "")
            note = "current behavior" if st == "ratified" else f"{st} — NOT current"
            lines.append(f"- [{label}]({s['path']}): {note}")

    if state.get("active_feature_packets"):
        lines += ["", "## Feature packets", ""]
        for p in state["active_feature_packets"]:
            req = f"{p['path'].rstrip('/')}/requirements.md"
            lines.append(f"- [{p.get('id', p['path'])}]({req}): "
                         f"{p.get('summary', '')}")

    lines += [
        "",
        "## Operating layer",
        "- [AGENTS.md](AGENTS.md): vendor-neutral agent instructions",
        "- [REVIEW.md](REVIEW.md): four-tier review gates",
        "",
        "## Generated context",
        "- [Agent context](docs/context/AGENT_CONTEXT.md): "
        "compact orientation for coding agents",
        "- [Source map](docs/context/SOURCE_MAP.md): "
        "corpus classification",
        "- [Full context](llms-full.txt): "
        "single-file export of canonical context + active packets",
    ]
    if INDEX_JSON.exists():
        lines.append("- [Document index](docs/DOCUMENT_INDEX.md): "
                     "semantic index of the document corpus")

    LLMS.write_text("\n".join(lines).rstrip() + "\n", encoding="utf-8")


# ---------------------------------------------------------------------------
# llms-full.txt  (improvement: includes active feature packet files)
# ---------------------------------------------------------------------------

def _section(label: str, content: str) -> str:
    sep = "=" * 78
    return f"\n{sep}\n# FILE: {label}\n{sep}\n\n{content}"


def write_llms_full(state: dict) -> None:
    proj_name = state.get("project", {}).get("name", "PROJECT")
    parts = [
        f"# {proj_name} — full context bundle",
        "# Generated by tools/context/build_context_pack.py.",
        "# Canonical docs + active feature packets for single-shot LLM ingestion.",
        "# Prefer editing source files, then rerun build_context_pack.py.",
        "",
    ]

    # 1. Canonical docs (the original set)
    for d in state.get("canonical_docs", []):
        content = read_text(d["path"], LLMS_FULL_DOC_MAX_CHARS)
        parts.append(_section(f"{d['path']}  ({d.get('role', '')})", content))

    # 1b. Capability specs — only 'ratified' is current behavior (substrate v2.2).
    for s in state.get("ratified_specs", []):
        st = s.get("status", "")
        content = read_text(s["path"], LLMS_FULL_DOC_MAX_CHARS)
        if st == "ratified":
            hdr = (f"RATIFIED SPEC (current behavior): {s['path']}  "
                   f"(capability {s.get('capability', '')})")
        else:
            hdr = (f"{st.upper()} SPEC — NOT current behavior: {s['path']}  "
                   f"(capability {s.get('capability', '')})")
        parts.append(_section(hdr, content))

    # 2. Active feature packets — each packet's files concatenated.
    #    This is the NEW behaviour: prior versions omitted packet content,
    #    leaving single-shot ingestion incomplete for in-flight work.
    for pkt in state.get("active_feature_packets", []):
        pkt_path = pkt.get("path", "")
        pkt_dir  = REPO_ROOT / pkt_path
        pkt_label = (f"FEATURE PACKET: {pkt.get('id', pkt_path)} — "
                     f"{pkt.get('summary', '')}")
        parts.append(f"\n{'#' * 78}\n# {pkt_label}\n{'#' * 78}")
        for fname in PACKET_FILES:
            fp = pkt_dir / fname
            if fp.exists():
                content = read_text(fp, LLMS_FULL_PACKET_MAX_CHARS)
                parts.append(_section(f"{pkt_path}/{fname}", content))
            else:
                parts.append(_section(f"{pkt_path}/{fname}",
                                      f"[MISSING: {pkt_path}/{fname}]"))

    LLMS_FULL.write_text("\n".join(parts).rstrip() + "\n", encoding="utf-8")


# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------

def main() -> int:
    ap = argparse.ArgumentParser(
        description="Generate the project agent context pack.")
    ap.add_argument("--check", action="store_true",
                    help="exit 1 if the pack is stale vs its inputs")
    args = ap.parse_args()

    state   = load_state()
    current = pack_inputs_hash(state)

    if args.check:
        recorded = (HASH_FILE.read_text(encoding="utf-8").strip()
                    if HASH_FILE.exists() else "")
        if recorded != current:
            print("DRIFT: context pack is stale — run build_context_pack.py",
                  file=sys.stderr)
            return 1
        print("context pack: fresh")
        return 0

    CTX.parent.mkdir(parents=True, exist_ok=True)
    write_context(state)
    write_source_map(state)
    write_llms(state)
    write_llms_full(state)
    HASH_FILE.write_text(current + "\n", encoding="utf-8")

    print(f"context pack written (inputs hash {current[:12]})")
    print(f"  {CTX.relative_to(REPO_ROOT)}")
    print(f"  {SOURCE_MAP.relative_to(REPO_ROOT)}")
    print(f"  {LLMS.relative_to(REPO_ROOT)}")
    print(f"  {LLMS_FULL.relative_to(REPO_ROOT)}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
