def classify_document(doc: dict, state: dict) -> str:
    """Classify a document entry from DOCUMENT_INDEX against PROJECT_STATE.

    PORTING: extend the middle section with project-specific path prefixes
    that carry semantic meaning in your repo layout.
    """
    path = doc["path"]
    canonical = {item["path"]: item.get("status", "canonical")
                 for item in state.get("canonical_docs", [])}
    superseded = {item["path"]
                  for item in state.get("historical_or_superseded_docs", [])}
    historical  = {item["path"]
                   for item in state.get("historical_docs", [])}
    active_dirs = [
        p["path"].rstrip("/") + "/"
        for p in state.get("active_feature_packets", [])
        if p.get("path")
    ]

    if path in canonical:
        return canonical[path]
    if path in superseded or path in historical:
        return "superseded"
    if any(path.startswith(d) for d in active_dirs):
        return "active-feature-packet"
    if path.startswith("specs/"):
        return "ratified-spec"
    if path.startswith("docs/features/_template/"):
        return "feature-template"
    if path.startswith("docs/context/"):
        return "generated-canonical"
    if path.startswith("docs/review-batch/"):
        return "review-evidence"
    if path.startswith("docs/superpowers/specs/"):
        return "locked-spec-evidence"
    if path.startswith("docs/superpowers/plans/"):
        return "plan-evidence"
    if path.startswith("tmp/"):
        return "temporary"
    if path.startswith(".remember/"):
        return "local-memory"
    if path.startswith("research/"):
        return "research-evidence"
    if path.endswith(".png"):
        return "visual-evidence"
    return "evidence"


def write_source_map(state: dict) -> None:
    if not INDEX_JSON.exists():
        SOURCE_MAP.write_text(
            "# Source Map\n\ndocs/DOCUMENT_INDEX.json not yet generated.\n",
            encoding="utf-8")
        return

    index = json.loads(INDEX_JSON.read_text(encoding="utf-8"))
    documents = index.get("documents") or index.get("files", [])
    generated_at = datetime.now(timezone.utc).isoformat(timespec="seconds")

    rows = []
    counts: Counter = Counter()
    families: dict[str, Counter] = defaultdict(Counter)
    for doc in documents:
        status = classify_document(doc, state)
        counts[status] += 1
        families[status][doc.get("family", "unknown")] += 1
        rows.append((status, doc))

    proj_name = state.get("project", {}).get("name", "project")
    lines = [
        f"# {proj_name} Source Map",
        "",
        "Status: generated",
        f"Generated: {generated_at}",
        "",
        ("This file classifies the indexed document corpus for agent use. "
         "Generated from `PROJECT_STATE.json` and `docs/DOCUMENT_INDEX.json`."),
        "",
        "## Counts",
        "",
        "| Status | Count |",
        "|---|---:|",
    ]
    for status, count in sorted(counts.items(),
                                key=lambda x: (-x[1], x[0])):
        lines.append(f"| `{status}` | {count} |")

    lines.extend(["", "## Canonical must-read docs", ""])
    for item in state.get("canonical_docs", []):
        if item.get("must_read"):
            lines.append(f"- `{item['path']}` — {item['role']} "
                         f"({item.get('status', '')})")

    hist = state.get("historical_or_superseded_docs",
                     state.get("historical_docs", []))
    if hist:
        lines.extend(["", "## Superseded or historical docs", ""])
        for item in hist:
            reason = item.get("reason") or item.get("note") or item.get("status", "")
            lines.append(f"- `{item['path']}` — {reason}")

    lines.extend(["", "## Full inventory classification", ""])
    current_status = None
    for status, doc in sorted(rows,
                               key=lambda x: (x[0], x[1]["path"].lower())):
        if status != current_status:
            current_status = status
            lines.extend(["", f"### {status}", ""])
        title = str(doc.get("title") or Path(doc["path"]).name).replace("|", "\\|")
        lines.append(
            f"- `{doc['path']}` — {title} "
            f"({doc.get('extension', '')}, {doc.get('size_kib', 0)} KiB)"
        )

    SOURCE_MAP.write_text("\n".join(lines).rstrip() + "\n", encoding="utf-8")


