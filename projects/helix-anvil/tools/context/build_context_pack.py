def render_auto(state: dict) -> str:
    proj    = state.get("project", {})
    posture = state.get("strategic_posture", {})
    focus   = state.get("current_focus", {})
    lines = [
        AUTO_START,
        f"# {proj.get('name', 'PROJECT')} — Agent Context (start here)",
        "",
        f"**One-liner:** {proj.get('one_liner', '')}",
        f"**Stage:** `{state.get('current_stage', '')}`"
        f"  ·  **Slug:** `{proj.get('slug', '')}`",
    ]
    if proj.get("market"):
        lines.append(f"**Market:** {proj['market']}")
    lines.append("")

    if posture:
        lines += ["## North star", ""]
        if posture.get("north_star"):
            lines.append(f"- **North star:** {posture['north_star']}")
        if posture.get("hard_promise"):
            lines.append(f"- **Hard promise:** {posture['hard_promise']}")
        if posture.get("wedge"):
            lines.append(f"- **Wedge:** {posture['wedge']}")
        if posture.get("what_it_is_not"):
            lines.append("- **What it is NOT:** "
                         + "; ".join(posture["what_it_is_not"]))
        lines.append("")

    if focus.get("primary"):
        lines += ["## Current goal", "", focus["primary"], ""]
    if focus.get("next_product_slice"):
        lines += ["Next product slice:", "", focus["next_product_slice"], ""]

    lines += ["## Read order", ""]
    for i, step in enumerate([
        "PROJECT_STATE.json (machine source of truth)",
        "this file (AGENT_CONTEXT.md)",
        "VISION.md",
        "BUILD_SPEC.md",
        "the active feature packet (next_product_slice below)",
        "docs/DOCUMENT_INDEX.md only for primary evidence",
    ], 1):
        lines.append(f"{i}. {step}")

    if state.get("source_of_truth_order"):
        lines += ["", "## Source-of-truth order (conflicts: earlier wins)", ""]
        for i, s in enumerate(state["source_of_truth_order"], 1):
            lines.append(f"{i}. {s}")

    if state.get("canonical_docs"):
        lines += ["", "## Canonical docs", ""]
        for d in state["canonical_docs"]:
            must = " *(must-read)*" if d.get("must_read") else ""
            lines.append(f"- `{d['path']}` — {d.get('role', '')}{must}")

    if state.get("ratified_specs"):
        lines += ["", "## Capability specs (ratified = current behavior)", ""]
        for s in state["ratified_specs"]:
            st = s.get("status", "")
            tag = "" if st == "ratified" else f"  -- NOT current ({st})"
            lines.append(f"- `{s['path']}` — capability "
                         f"`{s.get('capability', '')}` [{st}]{tag}")

    if state.get("active_feature_packets"):
        lines += ["", "## Active feature packets", ""]
        for p in state["active_feature_packets"]:
            lines.append(f"- `{p.get('path', '?')}` — "
                         f"[{p.get('status', '?')}] {p.get('summary', '')}")

    nxt = state.get("next_product_slice", {})
    if nxt:
        lines += ["", "## Next product slice",
                  f"- **{nxt.get('packet_id', '?')}**: "
                  f"{nxt.get('summary', '')}"]

    for item in state.get("do_not_start", []):
        pass  # rendered in block below if list non-empty
    if state.get("do_not_start"):
        lines += ["", "## Do NOT start yet", ""]
        for item in state["do_not_start"]:
            lines.append(f"- {item.get('item', '')} — "
                         f"_{item.get('reason', '')}_")

    if state.get("commands"):
        lines += ["", "## Commands", ""]
        for name, cmd in state["commands"].items():
            lines.append(f"- `{name}`: `{cmd}`")

    if state.get("agent_rules"):
        lines += ["", "## Hard agent rules", ""]
        for r in state["agent_rules"]:
            lines.append(f"- {r}")

    lines += ["", AUTO_END]
    return "\n".join(lines)


def existing_human_block() -> str:
    if CTX.exists():
        txt = CTX.read_text(encoding="utf-8")
        if HUMAN_START in txt and HUMAN_END in txt:
            return txt.split(HUMAN_START, 1)[1].split(HUMAN_END, 1)[0]
    return ("\n\n_Hand-authored orientation goes here (narrative the "
            "structured state cannot capture). This region is preserved "
            "across regeneration._\n\n")


def write_context(state: dict) -> None:
    auto  = render_auto(state)
    human = existing_human_block()
    CTX.parent.mkdir(parents=True, exist_ok=True)
    CTX.write_text(f"{auto}\n\n{HUMAN_START}{human}{HUMAN_END}\n",
                   encoding="utf-8")


